    public void displayIndexNo(){ 
       try{
            String get_programme  = ProSelect.getSelectedItem().toString();
            selectIndex.removeAllItems();
            selectIndex.addItem("Select IndexNo");
            String sql = "Select * from managestudentsresult where Programme = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, get_programme);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                selectIndex.addItem(rs.getString("IndexNo"));
            }
            
            }
            catch(Exception e){
               e.printStackTrace();
                
        }   
    }
   
    
