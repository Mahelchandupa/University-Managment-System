    private void DepSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DepSelectActionPerformed
        if(DepSelect.getSelectedItem().equals("School of Business")){
                selectIndex.removeAllItems();
                ProSelect.removeAllItems();
                ProSelect.setSelectedItem(null);
                ProSelect.addItem("Select Programme");
                ProSelect.addItem("Event Managment");
                ProSelect.addItem("Digital Marketing");
        }
        else if(DepSelect.getSelectedItem().equals("School of Computing")){
               selectIndex.removeAllItems();
               ProSelect.removeAllItems();
               ProSelect.setSelectedItem(null);
               ProSelect.addItem("Select Programme");
               ProSelect.addItem("Computer System Design");
               ProSelect.addItem("Software Engineering");
        }
        
        else if(DepSelect.getSelectedItem().equals("Select Student Department")){
               ProSelect.removeAllItems();
        }
    }//GEN-LAST:event_DepSelectActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(DepSelect.getSelectedIndex() == 0){
             JOptionPane.showMessageDialog(this, "Select Department");
        }
        else if(ProSelect.getSelectedIndex() == 0){
             JOptionPane.showMessageDialog(this, "Select Programme");
        }
        else if(selectIndex.getSelectedIndex() == 0){
             JOptionPane.showMessageDialog(this, "Select Your Index Number");
        }
        else{
            String getIndex = selectIndex.getSelectedItem().toString();
            String depa = DepSelect.getSelectedItem().toString();
            String prog = ProSelect.getSelectedItem().toString();   
            
       try{
             java.sql.Connection con = DBConnection.getConnection();
             java.sql.PreparedStatement pst = con.prepareStatement("Select * from managestudentsresult where Department = ? and Programme = ? and IndexNo =? ");
             
             pst.setString(1, depa);
             pst.setString(2, prog);
             pst.setString(3, getIndex);
             
             ResultSet rs = pst.executeQuery();
             if(rs.next()){
                StudentResultView studentResultView = new StudentResultView((getIndex));
                studentResultView.setVisible(true);
                this.dispose();
             }
             else{
               JOptionPane.showMessageDialog(this, "Invalid ");
             }
         }
        catch(Exception e){
              e.printStackTrace();
         }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

